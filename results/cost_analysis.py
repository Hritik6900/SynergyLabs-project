"""Cost model: self-hosted ChromaDB vs a managed vector DB (Pinecone), at scale.

Run:  python results/cost_analysis.py
Writes results/cost_comparison.md and prints the table.

Every assumption is a named constant below and is restated in the output, so the
comparison is transparent and easy to re-run with different numbers.
"""

from __future__ import annotations

import os

# --------------------------------------------------------------------------- #
# Assumptions (edit these; they are all printed into the report)              #
# --------------------------------------------------------------------------- #
EMBEDDING_DIM = 1536              # text-embedding-3-small
BYTES_PER_FLOAT = 4               # float32 vectors
METADATA_BYTES_PER_VECTOR = 500   # small per-chunk metadata (source, section, etc.)
INDEX_OVERHEAD_FACTOR = 2.0       # HNSW graph + copies roughly ~2x raw vector bytes
QUERIES_PER_MONTH = 1_000_000     # "lightly queried" workload assumption

# --- Self-hosted ChromaDB on a small cloud VM ---
# On-demand US prices, rounded to typical published rates (Jan 2026).
# Vectors live in RAM for HNSW, so the VM must fit index_bytes in memory.
VM_TIERS = [
    # (label, monthly_usd, ram_gb, disk_gb_included)
    ("2 vCPU / 8 GB (e.g. t3.large)",    60.0,   8,   100),
    ("4 vCPU / 16 GB (e.g. t3.xlarge)",  120.0,  16,  200),
    ("8 vCPU / 64 GB (e.g. r6i.2xlarge)", 400.0, 64,  500),
    ("16 vCPU / 128 GB (e.g. r6i.4xlarge)", 800.0, 128, 1000),
]
EBS_USD_PER_GB_MONTH = 0.08       # gp3 block storage, if disk must grow beyond included

# --- Managed: Pinecone serverless ---
# Serverless pricing (US, Jan 2026 published rates), storage + read/write units.
PINECONE_STORAGE_USD_PER_GB_MONTH = 0.33
PINECONE_READ_USD_PER_MILLION_RU = 8.25   # read units
PINECONE_WRITE_USD_PER_MILLION_WU = 2.00  # write units
# Simplifying RU/WU model: ~1 read unit per query for a small top-k on a modest
# index; 1 write unit per vector upsert (amortized to ~0 per month at steady state,
# so we count writes only as a one-time ingest and exclude from the monthly figure).
PINECONE_READ_UNITS_PER_QUERY = 1.0

# --- Managed: Pinecone pod-based (always-on), for the "always-on pods" contrast ---
# p1/s1-style pods. One pod holds ~1M 768-dim vectors; for 1536-dim, ~0.5M/pod.
PINECONE_POD_USD_PER_MONTH = 80.0         # ~$0.11/hr * 730 hr, standard pod
PINECONE_VECTORS_PER_POD_1536 = 500_000

SCALES = [100_000, 1_000_000, 10_000_000]


def raw_vector_bytes(n: int) -> int:
    return n * EMBEDDING_DIM * BYTES_PER_FLOAT


def index_bytes(n: int) -> int:
    """RAM-resident index size (vectors + metadata) with overhead."""
    return int((raw_vector_bytes(n) + n * METADATA_BYTES_PER_VECTOR) * INDEX_OVERHEAD_FACTOR)


def gb(n_bytes: int) -> float:
    return n_bytes / (1024 ** 3)


def chroma_monthly_cost(n: int) -> tuple[float, str]:
    """Pick the cheapest VM whose RAM fits the index; add disk overage if needed."""
    needed_ram_gb = gb(index_bytes(n))
    for label, monthly, ram_gb, disk_incl in VM_TIERS:
        if ram_gb >= needed_ram_gb:
            disk_needed_gb = gb(raw_vector_bytes(n) + n * METADATA_BYTES_PER_VECTOR)
            disk_overage = max(0.0, disk_needed_gb - disk_incl)
            cost = monthly + disk_overage * EBS_USD_PER_GB_MONTH
            return cost, label
    # Larger than our biggest tier: scale out with multiple biggest VMs.
    label, monthly, ram_gb, _ = VM_TIERS[-1]
    n_vms = -(-int(needed_ram_gb) // ram_gb)  # ceil
    return monthly * n_vms, f"{n_vms}x {label}"


def pinecone_serverless_monthly_cost(n: int) -> float:
    storage_gb = gb(raw_vector_bytes(n) + n * METADATA_BYTES_PER_VECTOR)
    storage_cost = storage_gb * PINECONE_STORAGE_USD_PER_GB_MONTH
    read_units = QUERIES_PER_MONTH * PINECONE_READ_UNITS_PER_QUERY
    read_cost = (read_units / 1_000_000) * PINECONE_READ_USD_PER_MILLION_RU
    return storage_cost + read_cost


def pinecone_pod_monthly_cost(n: int) -> tuple[float, int]:
    pods = max(1, -(-n // PINECONE_VECTORS_PER_POD_1536))  # ceil
    return pods * PINECONE_POD_USD_PER_MONTH, pods


def build_report() -> str:
    lines: list[str] = []
    lines.append("# Cost Comparison: Self-Hosted ChromaDB vs Managed Vector DB\n")
    lines.append(
        "Monthly cost to **store and serve** an index of N vectors, comparing a "
        "self-hosted ChromaDB instance against Pinecone (serverless and pod-based). "
        "Generated by `results/cost_analysis.py` — edit the constants there to "
        "re-run with your own numbers.\n"
    )

    lines.append("## Assumptions (stated explicitly)\n")
    lines.append(f"- **Embedding dimensionality**: {EMBEDDING_DIM} (text-embedding-3-small), float32 ({BYTES_PER_FLOAT} bytes/dim).")
    lines.append(f"- **Metadata**: ~{METADATA_BYTES_PER_VECTOR} bytes/vector (source filename, section, chunk index, etc.).")
    lines.append(f"- **Index overhead**: ~{INDEX_OVERHEAD_FACTOR}x raw vector bytes for the HNSW graph + working copies (must fit in RAM).")
    lines.append(f"- **Query volume**: {QUERIES_PER_MONTH:,} queries/month (a *lightly-queried* index — the scenario where managed always-on pods hurt most).")
    lines.append(f"- **ChromaDB**: runs embedded on one cloud VM (on-demand US prices); vectors held in RAM. Disk over the included amount billed at ${EBS_USD_PER_GB_MONTH}/GB-month.")
    lines.append(f"- **Pinecone serverless**: storage ${PINECONE_STORAGE_USD_PER_GB_MONTH}/GB-month + reads ${PINECONE_READ_USD_PER_MILLION_RU}/M read-units (~{PINECONE_READ_UNITS_PER_QUERY:.0f} RU/query); steady-state writes excluded.")
    lines.append(f"- **Pinecone pods**: always-on standard pod ~${PINECONE_POD_USD_PER_MONTH:.0f}/month, ~{PINECONE_VECTORS_PER_POD_1536:,} vectors/pod at {EMBEDDING_DIM}-dim.")
    lines.append("- Prices are published US list rates as of Jan 2026 and are approximate; the point is the *shape* of the curve, not the last dollar.\n")
    lines.append(
        "> Note: this table uses the **conservative** 1536-dim assumption. This "
        "project's default embeddings are local `all-MiniLM-L6-v2` at **384-dim** — "
        "4x smaller vectors, so storage/RAM (and thus the ChromaDB VM cost) are "
        "roughly **4x lower** than shown. The 1536-dim figures are the upper bound.\n"
    )

    # Per-scale sizing detail
    lines.append("## Index sizing per scale\n")
    lines.append("| Vectors | Raw vectors | Index in RAM (~2x) | On-disk |")
    lines.append("| --- | --- | --- | --- |")
    for n in SCALES:
        raw_gb = gb(raw_vector_bytes(n))
        idx_gb = gb(index_bytes(n))
        disk_gb = gb(raw_vector_bytes(n) + n * METADATA_BYTES_PER_VECTOR)
        lines.append(f"| {n:,} | {raw_gb:.2f} GB | {idx_gb:.2f} GB | {disk_gb:.2f} GB |")
    lines.append("")

    # Cost table
    lines.append("## Monthly cost by scale\n")
    lines.append("| Vectors | ChromaDB (self-hosted VM) | Pinecone serverless | Pinecone pods (always-on) |")
    lines.append("| --- | --- | --- | --- |")
    rows = []
    for n in SCALES:
        chroma_cost, chroma_label = chroma_monthly_cost(n)
        pc_sl = pinecone_serverless_monthly_cost(n)
        pc_pod, pods = pinecone_pod_monthly_cost(n)
        rows.append((n, chroma_cost, chroma_label, pc_sl, pc_pod, pods))
        lines.append(
            f"| {n:,} | **${chroma_cost:,.0f}/mo** ({chroma_label}) | "
            f"${pc_sl:,.0f}/mo | ${pc_pod:,.0f}/mo ({pods} pod{'s' if pods > 1 else ''}) |"
        )
    lines.append("")

    # Query-volume sensitivity: where does serverless cross a fixed VM?
    lines.append("## Query-volume sensitivity (1M-vector index)\n")
    lines.append(
        "The reads-are-free-after-the-VM property is the crux. For a fixed 1M-vector "
        "index (self-hosted VM = **$120/mo flat**, reads included), here is how "
        "Pinecone serverless scales with monthly query volume:\n"
    )
    n_ref = 1_000_000
    ref_vm_cost, _ = chroma_monthly_cost(n_ref)
    storage_ref = gb(raw_vector_bytes(n_ref) + n_ref * METADATA_BYTES_PER_VECTOR) * PINECONE_STORAGE_USD_PER_GB_MONTH
    lines.append("| Queries/month | ChromaDB VM (flat) | Pinecone serverless | Cheaper |")
    lines.append("| --- | --- | --- | --- |")
    for qpm in [100_000, 1_000_000, 10_000_000, 50_000_000, 100_000_000]:
        read_cost = (qpm * PINECONE_READ_UNITS_PER_QUERY / 1_000_000) * PINECONE_READ_USD_PER_MILLION_RU
        sl_cost = storage_ref + read_cost
        cheaper = "ChromaDB" if ref_vm_cost < sl_cost else "serverless"
        lines.append(f"| {qpm:,} | ${ref_vm_cost:,.0f}/mo | ${sl_cost:,.0f}/mo | {cheaper} |")
    lines.append(
        "\nServerless wins when the index is lightly queried; the fixed-cost VM wins "
        "once query volume is high enough that per-query read charges dominate. The "
        "crossover is the decision boundary between the two approaches.\n"
    )

    lines.append("## Reading the table\n")
    lines.append(
        "- At every scale, **self-hosted ChromaDB is a fixed VM cost** that grows in "
        "big steps only when the index no longer fits in RAM. It does not scale with "
        "query volume."
    )
    lines.append(
        "- **Pinecone pods** are the classic \"always-on pods\" cost: you pay per pod "
        "per hour whether or not you query, and pod count grows linearly with vectors "
        "— which is exactly the failure mode a large, lightly-queried index hits."
    )
    lines.append(
        "- **Pinecone serverless** removes the always-on premium and is competitive at "
        "small scale, but its storage + per-query read charges rise with both index "
        "size and query volume, so a high-QPS or very large index can overtake a single VM."
    )
    lines.append(
        "- **Trade-offs we accept for the ChromaDB cost win**: we self-manage the VM "
        "(patching, backups, monitoring), we get no built-in HA/replication or "
        "autoscaling, and a single-node index is bounded by one machine's RAM. Those "
        "are real operational costs the managed price is partly buying."
    )
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    report = build_report()
    out_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "cost_comparison.md")
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(report)
    print(report)
    print(f"\nWrote {out_path}")


if __name__ == "__main__":
    main()
